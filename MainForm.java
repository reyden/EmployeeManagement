    private void searchTheString(JTable table, String str){
        for(int i = 0; i < table.getRowCount(); i++){
            String value = table.getValueAt(i, 2).toString() + table
                    .getValueAt(i, 3).toString();
            if (value!=null && value.toLowerCase().matches(".*"+Pattern
                    .quote(str.toLowerCase())+".*"))
            {
                 table.setRowSelectionInterval(i, i);
                 
                 Rectangle r = table.getCellRect(i, 3, true);
                 table.scrollRectToVisible(r);
                 table.setDefaultRenderer(Object.class,new HighlightRenderer());
                 table.repaint();
                 
            }else{
                table.getModel();
                table.repaint();
            }
        }
    }
    
